package at.fhhagenberg.xml4.drugbank.logic;

public class TargetReporGeneratorException extends Exception {

	private static final long serialVersionUID = 1L;

	public TargetReporGeneratorException() {
		super();
	}

	public TargetReporGeneratorException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TargetReporGeneratorException(String message, Throwable cause) {
		super(message, cause);
	}

	public TargetReporGeneratorException(String message) {
		super(message);
	}

	public TargetReporGeneratorException(Throwable cause) {
		super(cause);
	}

}
